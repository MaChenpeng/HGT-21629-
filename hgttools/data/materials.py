def get_material(name: str, specification: str, material: str = "") -> Material:
    """根据名称、规格、材质获取材料信息"""
    key = (name, specification, material)
    if key in MATERIALS_DB:
        return MATERIALS_DB[key]
    # 尝试不指定材质查找
    key = (name, specification, "")
    if key in MATERIALS_DB:
        mat = MATERIALS_DB[key]
        # 返回副本并设置材质
        return Material(
            name=mat.name,
            specification=mat.specification,
            grade=mat.grade,
            unit=mat.unit,
            unit_weight=mat.unit_weight,
            tech_condition=mat.tech_condition,
            material_type=material or mat.material_type,
            unit_surface_area=mat.unit_surface_area
        )
    return Material(name, specification, None, "", 0, "", material, 0)


def get_material_by_code(code: str) -> str:
    """根据材质代码获取材质名称"""
    return MATERIAL_CODE_MAP.get(code, code)
